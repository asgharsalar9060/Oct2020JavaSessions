package OOP_Encapsulation;

public class Teachers {

	public String name;
	public int age;
	public String address;
	public boolean fullTime;
	
	public Teachers(String name, int age, String address, boolean fullTime) {
		this.name = name;
		this.age = age;
		this.address = address;
		this.fullTime = fullTime;
	}

}
